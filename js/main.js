$('#Read_more').click(function () {
    $('#Gestalt').css('display', 'block'),
        $('#Cennik').css('display', 'block'),
        $('#Home').css('display', 'none'),
        $('#Offer').css('display', 'none'),
        $('#Kontakt').css('display', 'none')
});
